export function getRandomVerse(book = 'all') {
  let verses = [];
  
  if (book === 'all') {
    // Get all verses from all books
    Object.values(BIBLE_VERSES).forEach(bookVerses => {
      verses = verses.concat(bookVerses);
    });
  } else {
    verses = BIBLE_VERSES[book] || BIBLE_VERSES.genesis;
  }
  
  // Add more randomness by shuffling the array first
  const shuffled = [...verses].sort(() => Math.random() - 0.5);
  const randomIndex = Math.floor(Math.random() * shuffled.length);
  return shuffled[randomIndex];
}
